    private void btnPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPurchaseActionPerformed
        double total = Double.parseDouble(txtDisplay.getText());
        double price = Double.parseDouble(txtPrice.getText());
        
        
        if (total >= price){
            try{ 
            double change = total - price;
            DecimalFormat format = new DecimalFormat("0.00");
            String formatted = format.format(change);
            txtChange.setText(String.valueOf(formatted));
            createFolder();
            readFile();
            removeQuantity();
            purchaseDetails();
            int a = JOptionPane.showConfirmDialog(null, "Here is your change:"+formatted, "Action Confirmation", JOptionPane.OK_OPTION);
                switch (a) {
                    case JOptionPane.YES_OPTION:
                        txtDisplay.setText("0.00");
                        txtChange.setText("");
                        frmItems Form = new frmItems();
                        Form.show();
                        dispose();
                        break;
                    case JOptionPane.NO_OPTION:
                        txtDisplay.setText("0.00");
                        txtChange.setText("");
                        this.toBack();
                        frmItems Form1 = new frmItems();
                        Form1.setVisible(true);
                        Form1.toFront();        
                        dispose();
                        break;
                    case JOptionPane.CLOSED_OPTION:
                        txtDisplay.setText("0.00");
                        txtChange.setText("");
                        this.toBack();
                        frmItems Form2 = new frmItems();
                        Form2.setVisible(true);
                        Form2.toFront();        
                        dispose();
                        break;
                    default:
                        break;
                }
            
            }
            catch(NumberFormatException ex){
             Logger.getLogger(frmItems.class.getName()).log(Level.SEVERE, null, ex);  
            } catch (IOException ex) {
                Logger.getLogger(frmItems.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InterruptedException ex) {
                Logger.getLogger(frmItems.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else {
        JOptionPane.showMessageDialog(null,"Money insufficient! Please insert more note", "warning", JOptionPane.WARNING_MESSAGE );
        }
        
    }//GEN-LAST:event_btnPurchaseActionPerformed

