    private void removeQuantity() throws IOException, InterruptedException{
        //Instantiating the File class
      String filePath = "C:\\Users\\ljk99\\Desktop\\APU\\APU Assignment\\Diploma In Software Engineering\\5. Fith Semester\\Java Programming\\Assignment\\Vending Machine\\VendingMachine\\ItemsData.txt";
      //Instantiating the Scanner class to read the file
      Scanner sc = new Scanner(new File(filePath));
      //instantiating the StringBuffer class
      StringBuffer buffer = new StringBuffer();
      //Reading lines of the file and appending them to StringBuffer
      while (sc.hasNextLine()) {
         buffer.append(sc.nextLine()+System.lineSeparator());
      }
      String fileContents = buffer.toString();
      //closing the Scanner object
      sc.close();
      int quantity = Integer.parseInt(txtTQuantity.getText());
      quantity-=1;
      String result = Integer.toString(quantity);
      String oldLine = txtProduct.getText()+ "," +txtPrice.getText() + "," + txtTQuantity.getText();
      String newLine = txtProduct.getText()+ "," +txtPrice.getText() + ","+ result;
      //Replacing the old line with new line
      fileContents = fileContents.replaceAll(oldLine, newLine);
      //instantiating the FileWriter class
      FileWriter writer = new FileWriter(filePath);
      writer.append(fileContents);
      writer.flush();
   }
    
